var variabe1 = null;
// var is a Function scoped variable

/* This means that even if the variable is 
 declared inside a function on line 14, it 
 can still be called on line 1 and used, 
 anywhere inside the scope of the function. */

// It can also be changed inside the function
/* This means it is a mutable variable. 
 Its value, data type, etc can be changed. */

let variabe2 = null;
// let is a Block scoped variable
/* This means the let variable can only be used
inside the block, which is denoted by the curly 
bracket, it is declared in. */
/* This means if you declare a let variable on 
line 14, it can only be used from line 14 onwards.
*/
